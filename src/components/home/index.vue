    circularLayout() {
      let count  = 6
      let size = 282
      // 旋转圆圈的半径
      let distance = 200
      let pointX = 80
      let pointY = 150
      let div = $('.developmentContent .circular')
      let len = div.length
      // console.log(div);
      for (let i = 0; i < len ; i ++){
        // 角度找到每一个球所对应的角度
        let angel = 360 / count * i
        // console.log(angel);
        // console.log(div[i]);
        var left = (pointX + distance * Math.cos(angel / 180 * Math.PI) ) / 46.875
        div[i].style.left = left + 'rem'
        // console.log(div[i].style.left);
        var top = (pointY +  distance * Math.sin(angel / 180 * Math.PI) + 10) / 46.875
        div[i].style.top =top + 'rem'
      }
    }
  }
};
