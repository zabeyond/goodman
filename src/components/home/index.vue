      if (text == "中文" || text == "Chinese") {
        this.$i18n.locale = "zh";
        this.text = this.$t("menu.中文");
        localStorage.setItem("text", this.text);
        localStorage.setItem("texttype", "zh");
      } else if (text == "英文" || text == "English") {
        this.$i18n.locale = "en";
        this.text = this.$t("menu.英文");
        localStorage.setItem("text", this.text);
        localStorage.setItem("texttype", "en");
